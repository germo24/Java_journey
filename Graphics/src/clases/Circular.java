    @Override

    public void paint(Graphics g) {

        try {

            super.paint(g);

            if (bandera == true) {

                String SVioleta = tfield1.getText();
                String SRojo = tfield2.getText();
                String SCeleste = tfield3.getText();

                int IVioleta = Integer.parseInt(SVioleta);
                int IRojo = Integer.parseInt(SRojo);
                int ICeleste = Integer.parseInt(SCeleste);

                int total = IVioleta + IRojo + ICeleste;

                float FVioleta = IVioleta;
                float FRojo = IRojo;
                float FCeleste = ICeleste;
                float Ftotal = total;

                double DGVioleta = (FVioleta * 360) / Ftotal;
                int GVioleta = (int) Math.ceil(DGVioleta);

                double DGRojo = (FRojo * 360) / Ftotal;
                int GRojo = (int) Math.ceil(DGRojo);

                double DGCeleste = (FCeleste * 360) / Ftotal;
                int GCeleste = (int) Math.ceil(DGCeleste);

                float PVioleta = (FVioleta * 100) / Ftotal;
                float PRojo = (FRojo * 100) / Ftotal;
                float PCeleste = (FCeleste * 100) / Ftotal;

                g.setColor(new Color(92, 84, 169));
                g.fillArc(40, 80, 200, 200, 0, GVioleta);
                g.fillRect(270, 120, 20, 20);
                g.drawString(" Color Violeta  %" + (double) Math.round(PVioleta * 10) / 10, 300, 140);

                g.setColor(new Color(243, 79, 63));
                g.fillArc(40, 80, 200, 200, GVioleta, GRojo);
                g.fillRect(270, 170, 20, 20);
                g.drawString(" Color Rojo  %" + (double) Math.round(PRojo * 10) / 10, 300, 190);

                g.setColor(new Color(0, 235, 210));
                g.fillArc(40, 80, 200, 200, GVioleta + GRojo, GCeleste);
                g.fillRect(270, 220, 20, 20);
                g.drawString(" Color Celeste  %" + (double) Math.round(PCeleste * 10) / 10, 300, 240);

            }

        } catch (NumberFormatException e) {

            JOptionPane.showMessageDialog(null, " Debe rellenar los campos correctamente para continuar", " Error ", JOptionPane.WARNING_MESSAGE);

        }
    }
